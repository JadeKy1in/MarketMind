HEDGE_CONDITIONAL: List[str] = [
    "if", "depending", "unless", "consider",
    "如果", "取决于", "除非", "考虑",
]

HEDGE_OBSERVATION: List[str] = [
    "note that", "worth watching", "it is worth", "consider the",
    "注意", "值得关注",
]

# ============================================================
# _HEDGING_PATTERNS — compiled regex for the test suite
# ============================================================

# Combined list of all "fuzzy" patterns to detect
_HEDGING_WORDS_SRC: List[str] = (
    PROBABILITY_QUALIFIERS
    + VAGUENESS_MARKERS
    + HEDGE_VERBS
    + FUTURE_UNCERTAINTY
    + HEDGE_LINKERS
    + HEDGE_TENTATIVE
    + HEDGE_CONDITIONAL
    + HEDGE_OBSERVATION
)

# Build a single regex pattern that matches any of the hedging words/phrases
def _build_hedging_regex() -> re.Pattern:
    """Build a compiled regex for the _HEDGING_PATTERNS test contract.

    The test suite expects _HEDGING_PATTERNS to be a compiled re.Pattern
    that can be used with .search(). It must match hedging language and
    NOT match clean/assertive sentences.

    The algorithm:
      1. Sort words longest-first to prefer multi-word matches over single.
      2. For CJK-containing words, use simple containment (no word-boundary).
      3. For ASCII words, use \\b word-boundary.
      4. Join all as alternations in a non-capturing group.
    """
    sorted_words = sorted(_HEDGING_WORDS_SRC, key=len, reverse=True)
    pieces: List[str] = []
    for word in sorted_words:
        esc = re.escape(word)
        has_cjk = any(ord(c) > 0x2E80 for c in word)
        if has_cjk:
            pieces.append(esc)
        else:
            pieces.append(r'\b' + esc + r'\b')
    # Build a single alternation pattern
    pattern_str = '|'.join(pieces)
    return re.compile(pattern_str, re.IGNORECASE)


_HEDGING_PATTERNS: re.Pattern = _build_hedging_regex()


# ============================================================
# Data structures
# ============================================================

@dataclass
class HedgeCheckResult:
    """Result of a zero-hedging check on a single assertion.

    Attributes:
        assertion: The original assertion text.
        is_clean: True if NO hedge words detected (assertion passes).
        detected_words: List of hedge words found in the assertion.
        categories_violated: Which categories were violated.
        recommendations: Suggested replacements.
    """

    assertion: str = ""
    is_clean: bool = True
    detected_words: List[str] = field(default_factory=list)
    categories_violated: List[str] = field(default_factory=list)
    recommendations: List[str] = field(default_factory=list)


@dataclass
class PredictionValidationResult:
    """Result of validating a single prediction's reasoning.

    Fields match the test suite contract:
      is_valid — True if reasoning contains no hedging language
      hedging_found — list of hedge words/phrases found
      sanitized_reasoning — reasoning with hedge words removed
    """

    is_valid: bool = True
    hedging_found: List[str] = field(default_factory=list)
    sanitized_reasoning: str = ""


