# Core validator functions
# ============================================================

def check_assertion_clean(
    assertion: str,
    custom_hedge_words: Optional[List[str]] = None,
) -> HedgeCheckResult:
    """Check an assertion against the zero-hedging protocol.

    Args:
        assertion: The assertion text to validate.
        custom_hedge_words: Optional additional hedge words to check.

    Returns:
        HedgeCheckResult with pass/fail and all violations.

    Example:
        >>> result = check_assertion_clean("TLT可能跌破支撑")
        >>> result.is_clean
        False
        >>> result.detected_words
        ['可能']
    """
    if not assertion or not assertion.strip():
        return HedgeCheckResult(
            assertion=assertion,
            is_clean=False,
            detected_words=["<empty assertion>"],
            categories_violated=["empty"],
            recommendations=["Assertion must not be empty"],
        )

    detected: List[str] = []
    violated_categories: List[str] = []

    # Use the compiled regex for a single-pass scan
    match = _HEDGING_PATTERNS.search(assertion)
    if match:
        detected.append(match.group())
        violated_categories.append("hedging")

    # Check custom words if provided
    if custom_hedge_words:
        for word in custom_hedge_words:
            has_cjk = any(ord(c) > 0x2E80 for c in word)
            if has_cjk:
                pat = re.compile(re.escape(word), re.IGNORECASE)
            else:
                pat = re.compile(r'\b' + re.escape(word) + r'\b', re.IGNORECASE)
            if pat.search(assertion):
                detected.append(word)
                if "custom" not in violated_categories:
                    violated_categories.append("custom")

    is_clean = len(detected) == 0

    # Generate basic recommendations
    recommendations: List[str] = []
    if not is_clean:
        recommendations.append(
            f"Replace fuzzy language with absolute assertions. "
            f"Detected {len(detected)} hedge word(s): {', '.join(detected[:5])}"
        )

    return HedgeCheckResult(
        assertion=assertion,
        is_clean=is_clean,
        detected_words=detected,
        categories_violated=violated_categories,
        recommendations=recommendations,
    )


def validate_scenario_predictions(
    predictions: List[str],
    strict_mode: bool = True,
) -> List[HedgeCheckResult]:
    """Batch-validate a list of predictions.

    In strict mode, ANY hedge word causes rejection.
    In lenient mode, only Category 1 + 3 (probability + hedge verbs) cause rejection.

    Args:
        predictions: List of assertion strings.
        strict_mode: If True, reject all hedge categories. If False, only
                     probability qualifiers and hedge verbs are checked.

    Returns:
        List of HedgeCheckResult, one per prediction.
    """
    results: List[HedgeCheckResult] = []
    for assertion in predictions:
        result = check_assertion_clean(assertion)
        results.append(result)
    return results


def clean_assertion(
    assertion: str,
    replacement_template: Optional[str] = None,
) -> str:
    """Attempt to clean an assertion by removing detected hedge words.

    This is a best-effort heuristic.  It simply removes detected hedge
    words from the assertion.  The caller should re-validate after cleaning.

    Args:
        assertion: The original assertion.
        replacement_template: If provided, replaces the entire assertion
            with this template (e.g. "{ticker} will close {relation} {price}").

    Returns:
        Cleaned assertion string.
    """
    if replacement_template:
        return replacement_template

    # Strip hedge words (ALL_HEDGE_WORDS — module-level, defined below but resolved at call time)
    from src.zero_hedging_validator import ALL_HEDGE_WORDS as _HEDGE_WORDS
    hedge_words = _HEDGE_WORDS
    cleaned = assertion
    for word in hedge_words:
        has_cjk = any(ord(c) > 0x2E80 for c in word)
        if has_cjk:
            pat = re.compile(re.escape(word), re.IGNORECASE)
        else:
            pat = re.compile(r'\b' + re.escape(word) + r'\b', re.IGNORECASE)
        cleaned = pat.sub("", cleaned)

    # Clean up extra spaces
    cleaned = re.sub(r"\s+", " ", cleaned).strip()
    return cleaned


# ============================================================
# ZeroHedgingValidator — wrapper class for pipeline integration
# ============================================================

class ZeroHedgingValidator:
    """Pipeline-integration wrapper for the Zero-Hedging Protocol.

    Provides validate_prediction() and validate_batch() methods that the
    shadow pipeline calls as a second-pass safety net.  This is a CLEAN pass,
    not a REJECT pass: it sanitises any remaining hedge words in predictions
    so the pipeline never outputs fuzzy language.

    Attributes:
        strict_mode: If True, ALL hedge categories trigger rejection.
                     If False, only probability + hedge verb categories.
    """

    def __init__(self, strict_mode: bool = True) -> None:
        self._strict_mode = strict_mode

    def validate_prediction(self, prediction: object) -> PredictionValidationResult:
        """Validate a ShadowPrediction's reasoning field.

        Args:
            prediction: A ShadowPrediction instance (must have .reasoning).

        Returns:
            PredictionValidationResult with is_valid, hedging_found, sanitized_reasoning.
        """
        reasoning = getattr(prediction, "reasoning", "")
        result = check_assertion_clean(reasoning)

        if result.is_clean:
            return PredictionValidationResult(
                is_valid=True,
                hedging_found=[],
                sanitized_reasoning=reasoning,
            )

        sanitized = clean_assertion(reasoning)
        return PredictionValidationResult(
            is_valid=False,
            hedging_found=result.detected_words,
            sanitized_reasoning=sanitized,
        )

    def validate_batch(self, batch: object) -> object:
        """Sanitise all predictions in a batch by cleaning hedge words.

        For each prediction, if the reasoning contains hedge words, the
        reasoning is cleaned (hedge words stripped).  This returns a new
        batch (or the same batch object) for chaining.

        Args:
            batch: A BatchShadowRun instance.

        Returns:
            The same batch instance (mutated in place) for chaining.
        """
        from src.shadow_types import BatchShadowRun, ShadowPrediction

        scenarios = getattr(batch, "scenarios", [])
        for scenario in scenarios:
            predictions = getattr(scenario, "predictions", [])
            for i, pred in enumerate(predictions):
                result = self.validate_prediction(pred)
                if not result.is_valid:
                    # Create a new prediction with sanitized reasoning
                    new_pred = ShadowPrediction(
                        target_ticker=pred.target_ticker,
                        target_type=pred.target_type,
                        predicted_value=pred.predicted_value,
                        comparison_operator=pred.comparison_operator,
                        prediction_id=pred.prediction_id,
                        scenario_id=pred.scenario_id,
                        scenario_type=getattr(pred, "scenario_type", None),
                        assertion=pred.assertion,
                        confidence=pred.confidence,
                        reasoning=result.sanitized_reasoning,
                        prediction_date=pred.prediction_date,
                        target_date=pred.target_date,
                        prediction_horizon_hours=pred.prediction_horizon_hours,
                        source_decision_track=pred.source_decision_track,
                        was_safety_valve_bypassed=pred.was_safety_valve_bypassed,
                        original_safety_valves=list(pred.original_safety_valves),
                        resolved_at=pred.resolved_at,
                        verdict=pred.verdict,
                    )
                    # Replace in list (dataclass is frozen)
                    predictions[i] = new_pred

        return batch

    @property
    def strict_mode(self) -> bool:
        return self._strict_mode


