"""
zero_hedging_validator.py — Phase 8.1 Zero-Hedging Protocol (I1 Enforcer)

The core constraint engine for Shadow Mode's absolute assertion discipline.
Every assertion generated by the shadow mode pipeline MUST pass through this
validator.  Any assertion containing fuzzy / hedge words is REJECTED.

Hedge word categories (Phase 8.1 blueprint §Zero-Hedging Protocol):
  1. Probability qualifiers — "可能", "或许", "maybe", "possibly", "perhaps"
  2. Vagueness markers     — "面临压力", "面临不确定性", "somewhat", "relatively"
  3. Hedge verbs           — "may", "might", "could", "would", "seems", "appears"
  4. Future uncertainty    — "面临风险", "不确定", "uncertain", "unclear"

Design decision — pure-function validator (no state) with configurable word lists.
Supports both Chinese and English hedge detection.

SPARC:
  Specification: reject assertions containing fuzzy words, return structured report.
  Pseudocode: word-list scan + regex boundary matching.
  Architecture: configurable word lists, pure functions, no I/O.
  Refinement: optimised to O(n) single-pass scanning per assertion.
  Completion: exhaustive word list coverage (26 words across 4 categories).
"""

from __future__ import annotations

import re
from dataclasses import dataclass, field
